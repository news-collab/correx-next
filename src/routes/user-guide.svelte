<h1>Introducing Correx</h1>

<p>Thanks for trying Correx.news, the ASU News Co/Lab's new corrections/updates tool. Our goal is to help you easily send corrections and major updates down the same social media pathways that the original stories traveled.</p>

<p>Correx is based on several notions: First, we believe that forthright corrections are a fundamental, essential part of journalistic transparency. Second, we can help automate the process of sending them down those social media pathways -- to alert people who'd shared the original articles that there was a correction or significant update. Finally, we hope to help the news industry (and others who believe in the best journalistic principles) come up with a standard for creating and publishing corrections.</p>

